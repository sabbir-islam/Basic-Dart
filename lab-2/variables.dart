void main(){

    // variable are used in dart are Var , final
    // var is used to declare a variable that can be changed later
    // final is used to declare a variable that can't be changed later

    var name = "Sabbir Islam";
    final age = 21;

    print("Hello i'm $name and i'm $age years old");

    // data types are used in dart are int , double , String , bool, list
    int number = 10;
    // int used to store integer value
    double decimal = 10.5;
    // double used to store decimal value
    String text = "Hello World";
    // String used to store text value
    bool isTrue = true;
    // bool used to store boolean value
    List list = [1,2,3,4,5];
    // list used to store multiple values

    print(number);
    print(decimal);
    print(text);
    print(isTrue);
    print(list);

}